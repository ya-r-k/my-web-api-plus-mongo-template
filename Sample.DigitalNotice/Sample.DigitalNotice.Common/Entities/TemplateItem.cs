namespace Sample.DigitalNotice.Common.Entities;

/// <summary>
/// Represents a template item.
/// </summary>
public class TemplateItem
{
    /// <summary>
    /// Gets or sets the number of the template item.
    /// </summary>
    public uint Number { get; set; }

    /// <summary>
    /// Gets or sets the name of the template item.
    /// </summary>
    public string Name { get; set; }

    /// <summary>
    /// Gets or sets the description of the template item.
    /// </summary>
    public string Description { get; set; }
}
